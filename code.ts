// Tangani pemilihan ikon dari UI
figma.ui.onmessage = (msg) => {
  if (msg.type === "ICON_SELECTED") {
    // Ambil node yang dipilih
    const selection = figma.currentPage.selection;
    const selectedFrame = selection.find((node) => node.type === "FRAME");

    // Ambil data SVG dari file yang dipilih
    fetch(msg.icon)
      .then((response) => response.text())
      .then((svg) => {

        // Buat node SVG dan tambahkan ke canvas
        const svgNode = figma.createNodeFromSvg(svg);
        figma.currentPage.appendChild(svgNode);
        figma.viewport.scrollAndZoomIntoView([svgNode]);

        // Jika ada frame yang dipilih, tambahkan ikon ke dalam frame tersebut
        if (selectedFrame) {
          selectedFrame.appendChild(svgNode);
        } else {
          // Jika tidak ada frame yang dipilih, tambahkan ikon ke halaman saat ini
          figma.currentPage.appendChild(svgNode);
        }

        // Atur posisi ikon di tengah frame atau halaman
        svgNode.x = selectedFrame
          ? selectedFrame.width / 2 - 25
          : figma.viewport.center.x;
        svgNode.y = selectedFrame
          ? selectedFrame.height / 2 - 25
          : figma.viewport.center.y;

        figma.viewport.scrollAndZoomIntoView([svgNode]);

        // Sembunyikan spinner setelah ikon berhasil di-load
        figma.ui.postMessage({ type: "HIDE_SPINNER" });
      })
      .catch((error) => {
        console.error("Error fetching SVG:", error);
        figma.ui.postMessage({ type: "HIDE_SPINNER" }); // Sembunyikan spinner jika terjadi kesalahan
      });
  }
};
